
CREATE TABLE CLIENTE
( 
	IDCLIENTE            integer  NOT NULL ,
	NOMCLIENTE           varchar(150)  NOT NULL ,
	CONSTRAINT XPKCLIENTE PRIMARY KEY (IDCLIENTE ASC)
)
go



CREATE TABLE PEDIDO
( 
	IDPEDIDO             integer  NOT NULL ,
	FECHA                datetime  NOT NULL ,
	IDCLIENTE            integer  NOT NULL ,
	CONSTRAINT XPKPEDIDO PRIMARY KEY (IDPEDIDO ASC),
	CONSTRAINT FK_PEDIDO_CLIENTE FOREIGN KEY (IDCLIENTE) REFERENCES CLIENTE(IDCLIENTE)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE PRODUCTO
( 
	IDPRODUCTO           integer  NOT NULL ,
	NOMPRODUCTO          varchar(20)  NULL ,
	CONSTRAINT XPKPRODUCTO PRIMARY KEY (IDPRODUCTO ASC)
)
go



CREATE TABLE DETALLE_PEDIDO
( 
	CANTIDAD             integer  NOT NULL ,
	IDPEDIDO             integer  NOT NULL ,
	IDPRODUCTO           integer  NOT NULL ,
	CONSTRAINT XPKDETALLE_PEDIDO PRIMARY KEY (IDPEDIDO ASC,IDPRODUCTO ASC),
	CONSTRAINT FK_DETALLE_PEDIDO_PEDIDO FOREIGN KEY (IDPEDIDO) REFERENCES PEDIDO(IDPEDIDO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_DETALLE_PEDIDO_PRODUCT0 FOREIGN KEY (IDPRODUCTO) REFERENCES PRODUCTO(IDPRODUCTO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go


